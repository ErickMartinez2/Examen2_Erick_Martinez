    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        try {
            if (arma_3.isSelected() && Integer.parseInt(tf_edad.getText()) < 25) {
                JOptionPane.showMessageDialog(this, "Demasiado joven para una RPG-7!");
            } else {
                if (Integer.parseInt(tf_edad.getText()) > 17) {
                    String Arma = "";
                    int fuego = 0;
                    if (arma_1.isSelected()) {
                        Arma = "AK-47";
                        fuego = 27;
                    } else {
                        if (arma_2.isSelected()) {
                            Arma = "Revolver_Navant";
                            fuego = 13;
                        } else {
                            if (arma_3.isSelected()) {
                                Arma = "RPG-7";
                                fuego = 57;
                            }
                        }
                    }
                    Rusia.add(new Rusos(tf_nombre.getText(), Integer.parseInt(tf_id.getText()), Integer.parseInt(tf_edad.getText()), tf_rango.getText(), Integer.parseInt(tf_edad.getText()) * 8, Arma, fuego));
                    JOptionPane.showMessageDialog(this, "Ruso Agregado Exitosamente!");
                    tf_nombre.setText("");
                    tf_id.setText("");
                    tf_edad.setText("");
                    tf_rango.setText("");
                    tf_resistencia.setText("");
                    arma_1.setSelected(false);
                    arma_2.setSelected(false);
                    arma_3.setSelected(false);
                    tf_fuego.setText("");
                } else {
                    JOptionPane.showMessageDialog(this, "El soldado no esta calificado por ser menor de edad!");
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Datos Incorrectos!");
        }
    }//GEN-LAST:event_jButton1MouseClicked

